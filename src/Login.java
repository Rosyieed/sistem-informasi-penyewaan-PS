    private void textPassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textPassKeyPressed
        try {
            // TODO add your handling code here:
            Connection conn = koneksiDatabase.getKoneksi();
            Statement st = conn.createStatement();
            
            String sql = "SELECT * FROM data_user WHERE Username='" + textUsername.getText()
                    +"' AND Password='" + textPass.getText() + "'";
            ResultSet rs = st.executeQuery(sql);
            
            if (evt.getKeyCode() == KeyEvent.VK_ENTER && rs.next())
            {
                Beranda hm = new Beranda();
                hm.setVisible(true);
                hm.pack();
                hm.setLocationRelativeTo(null);
                hm.setDefaultCloseOperation(Login.EXIT_ON_CLOSE);
                dispose();
            }
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Username atau Password SALAH!");
        }
    }//GEN-LAST:event_textPassKeyPressed

