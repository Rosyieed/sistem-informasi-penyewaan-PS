    private void btLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLoginActionPerformed
        try {
            // TODO add your handling code here:
            Connection conn = koneksiDatabase.getKoneksi();
            Statement st = conn.createStatement();
            
            String sql = "SELECT * FROM data_user WHERE Username='" + textUsername.getText()
                    +"' AND Password='" + textPass.getText() + "'";
            ResultSet rs = st.executeQuery(sql);
            
            if (rs.next())
            {
                Beranda hm = new Beranda();
                hm.setVisible(true);
                hm.pack();
                hm.setLocationRelativeTo(null);
                hm.setDefaultCloseOperation(Login.EXIT_ON_CLOSE);
                dispose();
            }
            
            else
            {
                JOptionPane.showMessageDialog(rootPane, "Username atau Password SALAH!");
                clrscr();
                
            } 
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btLoginActionPerformed

